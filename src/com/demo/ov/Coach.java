package com.demo.ov;

public class Coach {
    private String ID;
    private int grade;
    private String sit_name;
    private int sit_total;
    private float sit_price;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public int getGrade() {
        return grade;
    }

    public void setGrade(int grade) {
        this.grade = grade;
    }

    public String getSit_name() {
        return sit_name;
    }

    public void setSit_name(String sit_name) {
        this.sit_name = sit_name;
    }

    public int getSit_total() {
        return sit_total;
    }

    public void setSit_total(int sit_total) {
        this.sit_total = sit_total;
    }

    public float getSit_price() {
        return sit_price;
    }

    public void setSit_price(float sit_price) {
        this.sit_price = sit_price;
    }
}
